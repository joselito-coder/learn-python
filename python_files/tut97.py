# Flask vs Django
# If you want speed and you want to build something from ground level and you want full control you should use flask
#  If you want something  of "Kitchen Sink" type (https://idioms.thefreedictionary.com/everything+but+the+kitchen+sink) which means you want everything prebuilt, or if you want something prebuilt and want to build a website quickly, Then use Django (slower compared to flask)

# Why web dev using python?
# Because python has some prebuilt libraries like tensor flow , tensorflow hub and you don't have to write from scratch also you don't need to learn other backend languages like javascript or php
# You can also create quick apis using python eg. api giving data on Indian railways and returning response in json
